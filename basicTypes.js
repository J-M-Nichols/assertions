/**
 * Defines an enum of basic javascript types.
 * 
 * @author Johnathan M. Nichols
 */
const basicTypes = Object.freeze({
    string: 'string',
    number: 'number',
    boolean: 'boolean',
    bigint: 'bigint',
    symbol: 'symbol',
    null: 'null',
    undefined: 'undefined',
    function: 'function',
});

export default basicTypes;